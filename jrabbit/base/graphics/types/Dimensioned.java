package org.jrabbit.base.graphics.types;

/*****************************************************************************
 * An object that is Dimensioned is viewed as having a "space" in the game
 * world. For simplicity, this is conceived of as a width and a height.
 * 
 * NOTE: The width and height values do not need to be the "final" dimensions,
 * only the base values - it's intended that a Dimensioned object can also be
 * scaled beyond this, so it's "effective" width and height may be more or less.
 * 
 * @author Chris Molini
 *****************************************************************************/
public interface Dimensioned
{
	/*************************************************************************
	 * Learns about the dimensions of the object.
	 * 
	 * @return The base width of the Dimensioned object.
	 *************************************************************************/
	public float width();

	/*************************************************************************
	 * Learns about the dimensions of the object.
	 * 
	 * @return The base height of the Dimensioned object.
	 *************************************************************************/
	public float height();
}