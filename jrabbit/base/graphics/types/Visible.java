package org.jrabbit.base.graphics.types;

/*****************************************************************************
 * An object that is visible has a flag to indicate whether or not it should be
 * rendered.
 * 
 * @author Chris Molini
 *****************************************************************************/
public interface Visible
{
	/*************************************************************************
	 * Determines whether the object is visible.
	 * 
	 * @return True if the object should be rendered, false if not.
	 *************************************************************************/
	public boolean visible();

	/*************************************************************************
	 * Redefines the visibility flag.
	 * 
	 * @param visible
	 *            Whether or not the object should be visible.
	 ************************************************************************/
	public void setVisible(boolean visible);
}